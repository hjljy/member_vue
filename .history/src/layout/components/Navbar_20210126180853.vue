<template>
  <div class="navbar">
    <hamburger :is-active="sidebar.opened" class="hamburger-container" @toggleClick="toggleSideBar" />

    <breadcrumb class="breadcrumb-container" />

    <div class="right-menu">
      <el-dropdown class="avatar-container" trigger="click">
        <div class="avatar-wrapper">
          <img :src="avatar+'?imageView2/1/w/80/h/80'" class="user-avatar">
          <i class="el-icon-caret-bottom" />
        </div>
        <el-dropdown-menu slot="dropdown" class="user-dropdown">
          <router-link to="/">
            <el-dropdown-item>
              Home
            </el-dropdown-item>
          </router-link>

          <el-dropdown-item divided @click.native="bindPhone">关联手机号码</el-dropdown-item>

          <el-dropdown-item divided @click.native="rsetpwd">修改账号密码</el-dropdown-item>

          <el-dropdown-item divided @click.native="logout">
            <span style="display:block;">退出登陆</span>
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
    </div>
    <el-dialog
      title="提示"
      :visible.sync="centerDialogVisible"
      width="20%"
      center
    >
      <el-form :model="passwordParam" label-width="80px">
        <el-form-item label="旧密码">
          <el-input v-model="passwordParam.oldPassword" autocomplete="off" />
        </el-form-item>
        <el-form-item label="新密码">
          <el-input v-model="passwordParam.newPassword" autocomplete="off" />
        </el-form-item>
        <el-form-item label="重复密码">
          <el-input v-model="passwordParam.rPassword" autocomplete="off" />
        </el-form-item>
      </el-form>
      <span slot="footer" class="dialog-footer">
        <el-button @click="centerDialogVisible = false">取 消</el-button>
        <el-button type="primary" @click="rsetpwd()">确 定</el-button>
      </span>
    </el-dialog>
  </div>

</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import Hamburger from '@/components/Hamburger'

export default {
  components: {
    Breadcrumb,
    Hamburger
  },
  data() {
    return {
      passwordParam: {
        oldPassword: '',
        newPassword: '',
        rPassword: ''
      },

      centerDialogVisible: false
    }
  },
  computed: {
    ...mapGetters([
      'sidebar',
      'avatar'
    ])
  },
  methods: {
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },
    async logout() {
      await this.$store.dispatch('user/logout')
      this.$router.push(`/login?redirect=${this.$route.fullPath}`)
    },
    async bindPhone() {
      await this.$store.dispatch('user/logout')
      this.$router.push(`/login?redirect=${this.$route.fullPath}`)
    },
    // async rsetpwd() {
    //   await this.$store.dispatch('user/resetPassword')
    //   this.$router.push(`/login?redirect=${this.$route.fullPath}`)
    // },
    async rsetpwd() {
      await this.$store.dispatch('user/resetPassword', this.passwordParam)
      this.$router.push(`/login?redirect=${this.$route.fullPath}`)
      this.centerDialogVisible = true
    }
  }
}
</script>

